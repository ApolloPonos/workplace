package edu.java.puzzle;

public class ifElse8 {

	
	public static void main(String[] args) {
		int puan = 66;

		if (puan < 25)
			System.out.println("0");
		else if (puan < 45)
			System.out.println("1");
		else if (puan < 55)
			System.out.println("2");
		else if (puan < 70)
			System.out.println("3");
		else if (puan < 85)
			System.out.println("4");
		else
			System.out.println("5");

	}

}
